//! # branchdb
//!
//! AI 에이전트를 위한 임베디드 · 브랜치 가능한 키-값 저장소.
//!
//! 이 데이터베이스를 정의하는 단 하나의 기술: **O(1) 브랜치**. 데이터셋 전체를
//! 상수 시간에, 데이터 복사 0으로 분기할 수 있다. 데이터가 *영속 불변 트리*
//! 안에 살고, 브랜치들이 바뀌지 않은 노드를 전부 공유하기 때문이다(구조 공유).
//! Git이 커밋을 저장하는 것과 같은 원리다.
//!
//! 계층 구조(아래에서 위로):
//! - [`tree`]: 영속 불변 트리 (엔진).
//! - [`db`]: `fork` / `put` / `get` / `delete`를 노출하는 브랜치 관리자 ([`db::BranchDB`]).

